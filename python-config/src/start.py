def soma(num_1: int, num_2: int) -> int:
    """Somando dois números
    :param - num_1: primeiro número da soma
           - num_2: segundo número da soma
    :return - Soma
    """
    soma_dois_numeros = num_1 + num_2
    return soma_dois_numeros


